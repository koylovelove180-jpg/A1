#!/usr/bin/env node
/**
 * Migration script: lessonData.js → PostgreSQL INSERT statements
 *
 * Usage:
 *   node scripts/migrate-lesson-data-to-postgres.mjs
 *   node scripts/migrate-lesson-data-to-postgres.mjs --course=cooking-basics --out=database/seed-cooking-basics.sql
 *
 * Requires Node 18+ (dynamic import of ESM lessonData).
 */

import { writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { lessonData } from '../src/data/lessonData.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

const args = Object.fromEntries(
  process.argv.slice(2).map((arg) => {
    const [key, value] = arg.replace(/^--/, '').split('=');
    return [key, value ?? true];
  }),
);

const courseSlug = args.course || 'cooking-basics';
const orgSlug = args.org || 'default-org';
const moduleSlug = args.module || 'main';
const locale = args.locale || 'th';
const outPath = resolve(root, args.out || `database/seed-${courseSlug}.sql`);

function sqlString(value) {
  if (value == null) return 'NULL';
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlJson(value) {
  return `'${JSON.stringify(value).replace(/'/g, "''")}'::jsonb`;
}

function mapPageType(type) {
  const map = {
    home: 'home',
    cover: 'cover',
    article: 'article',
    'cards-grid': 'cards_grid',
    'split-advanced': 'split_advanced',
    methods: 'methods',
    'quiz-interactive': 'quiz_interactive',
  };
  return map[type] || type;
}

function buildContentPayload(page) {
  const { id, type, title, subtitle, quizMode, questions, ...rest } = page;
  if (type === 'quiz-interactive') {
    return { pass_threshold: 0.6 };
  }
  return rest;
}

function generateSql(pages) {
  const lines = [];
  lines.push('-- Auto-generated by migrate-lesson-data-to-postgres.mjs');
  lines.push('BEGIN;');
  lines.push('');
  lines.push(`INSERT INTO organizations (slug, name) VALUES (${sqlString(orgSlug)}, ${sqlString('Default Organization')}) ON CONFLICT (slug) DO NOTHING;`);
  lines.push(`INSERT INTO courses (org_id, slug, default_locale, is_published)`);
  lines.push(`  SELECT id, ${sqlString(courseSlug)}, ${sqlString(locale)}, true FROM organizations WHERE slug = ${sqlString(orgSlug)}`);
  lines.push(`  ON CONFLICT (org_id, slug) DO NOTHING;`);
  lines.push('');
  lines.push(`INSERT INTO course_translations (course_id, locale, title, subtitle)`);
  lines.push(`  SELECT c.id, ${sqlString(locale)}, ${sqlString(pages[0]?.title || courseSlug)}, ${sqlString(pages[0]?.subtitle || '')}`);
  lines.push(`  FROM courses c JOIN organizations o ON c.org_id = o.id`);
  lines.push(`  WHERE o.slug = ${sqlString(orgSlug)} AND c.slug = ${sqlString(courseSlug)}`);
  lines.push(`  ON CONFLICT (course_id, locale) DO UPDATE SET title = EXCLUDED.title, subtitle = EXCLUDED.subtitle;`);
  lines.push('');
  lines.push(`INSERT INTO course_modules (course_id, sort_order, slug)`);
  lines.push(`  SELECT c.id, 1, ${sqlString(moduleSlug)} FROM courses c JOIN organizations o ON c.org_id = o.id`);
  lines.push(`  WHERE o.slug = ${sqlString(orgSlug)} AND c.slug = ${sqlString(courseSlug)}`);
  lines.push(`  ON CONFLICT (course_id, slug) DO NOTHING;`);
  lines.push('');
  lines.push(`INSERT INTO classrooms (course_id, org_id, slug, name)`);
  lines.push(`  SELECT c.id, o.id, ${sqlString(courseSlug)}, ${sqlString('ห้องเรียนทั่วไป')}`);
  lines.push(`  FROM courses c JOIN organizations o ON c.org_id = o.id`);
  lines.push(`  WHERE o.slug = ${sqlString(orgSlug)} AND c.slug = ${sqlString(courseSlug)}`);
  lines.push(`  ON CONFLICT (course_id, slug) DO NOTHING;`);
  lines.push('');

  pages.forEach((page, index) => {
    const sortOrder = index + 1;
    const pageType = mapPageType(page.type);
    const quizMode = page.quizMode ? sqlString(page.quizMode) : 'NULL';
    const content = buildContentPayload(page);
    const pageVar = `page_${page.id}`;

    lines.push(`-- Page ${page.id}: ${page.type}`);
    lines.push(`WITH module_ref AS (`);
    lines.push(`  SELECT m.id AS module_id FROM course_modules m`);
    lines.push(`  JOIN courses c ON m.course_id = c.id`);
    lines.push(`  JOIN organizations o ON c.org_id = o.id`);
    lines.push(`  WHERE o.slug = ${sqlString(orgSlug)} AND c.slug = ${sqlString(courseSlug)} AND m.slug = ${sqlString(moduleSlug)}`);
    lines.push(`), inserted_page AS (`);
    lines.push(`  INSERT INTO lesson_pages (module_id, sort_order, legacy_page_id, page_type, quiz_mode, content)`);
    lines.push(`  SELECT module_id, ${sortOrder}, ${page.id}, ${sqlString(pageType)}, ${quizMode}, ${sqlJson(content)} FROM module_ref`);
    lines.push(`  ON CONFLICT (module_id, sort_order) DO UPDATE SET legacy_page_id = EXCLUDED.legacy_page_id, page_type = EXCLUDED.page_type, quiz_mode = EXCLUDED.quiz_mode, content = EXCLUDED.content`);
    lines.push(`  RETURNING id`);
    lines.push(`)`);
    lines.push(`INSERT INTO page_translations (page_id, locale, title, subtitle, body)`);
    lines.push(`  SELECT id, ${sqlString(locale)}, ${sqlString(page.title || '')}, ${sqlString(page.subtitle || '')}, ${sqlJson({})} FROM inserted_page`);
    lines.push(`  ON CONFLICT (page_id, locale) DO UPDATE SET title = EXCLUDED.title, subtitle = EXCLUDED.subtitle;`);
    lines.push('');

    if (page.type === 'quiz-interactive' && Array.isArray(page.questions)) {
      page.questions.forEach((question, qIndex) => {
        const qNum = qIndex + 1;
        lines.push(`WITH page_ref AS (`);
        lines.push(`  SELECT lp.id AS page_id FROM lesson_pages lp`);
        lines.push(`  JOIN course_modules m ON lp.module_id = m.id`);
        lines.push(`  JOIN courses c ON m.course_id = c.id`);
        lines.push(`  JOIN organizations o ON c.org_id = o.id`);
        lines.push(`  WHERE o.slug = ${sqlString(orgSlug)} AND c.slug = ${sqlString(courseSlug)} AND lp.legacy_page_id = ${page.id} AND lp.sort_order = ${sortOrder}`);
        lines.push(`), inserted_q AS (`);
        lines.push(`  INSERT INTO quiz_questions (page_id, sort_order, correct_option_index)`);
        lines.push(`  SELECT page_id, ${qNum}, ${question.answer} FROM page_ref`);
        lines.push(`  RETURNING id, page_id`);
        lines.push(`)`);
        lines.push(`INSERT INTO quiz_question_translations (question_id, locale, prompt, hint, image_url, image_alt)`);
        lines.push(`  SELECT id, ${sqlString(locale)}, ${sqlString(question.q)}, ${sqlString(question.hint || null)}, ${sqlString(question.image || null)}, ${sqlString(question.imageAlt || null)} FROM inserted_q;`);

        question.options.forEach((option, oIndex) => {
          lines.push(`WITH q_ref AS (`);
          lines.push(`  SELECT qq.id AS question_id FROM quiz_questions qq`);
          lines.push(`  JOIN lesson_pages lp ON qq.page_id = lp.id`);
          lines.push(`  WHERE lp.legacy_page_id = ${page.id} AND qq.sort_order = ${qNum}`);
          lines.push(`), inserted_o AS (`);
          lines.push(`  INSERT INTO quiz_options (question_id, sort_order)`);
          lines.push(`  SELECT question_id, ${oIndex + 1} FROM q_ref RETURNING id`);
          lines.push(`)`);
          lines.push(`INSERT INTO quiz_option_translations (option_id, locale, label)`);
          lines.push(`  SELECT id, ${sqlString(locale)}, ${sqlString(option)} FROM inserted_o;`);
        });
        lines.push('');
      });
    }
  });

  lines.push('COMMIT;');
  lines.push('');
  return lines.join('\n');
}

const sql = generateSql(lessonData);
writeFileSync(outPath, sql, 'utf8');

console.log(`Wrote ${lessonData.length} pages to ${outPath}`);
